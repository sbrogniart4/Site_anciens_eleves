    L’Association des Anciens Elèves du Lycée-Collège Raymond Poincaré de Bar le Duc (dont l’origine remonte aux origines du Lycée (1859) [le Collège communal Gilles-de Trèves devenu fugitivement Lycée avant de se déplacer ici en 1857] a beaucoup compté dans la vie du Lycée tant par ses présidents de renom (Poincaré, Theuriet) que par ses actions. Il était un lieu d’amitié pour les anciens, mais, surtout un lieu de devoir et de reconnaissance. Servir le Lycée qui leur avait permis de s’élever dans le savoir et la vie. D’où l’origine du prix. Je vais y revenir.




				